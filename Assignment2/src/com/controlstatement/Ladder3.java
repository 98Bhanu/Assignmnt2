public class Ladder3 {

	public static void main(String[] args) {
		int i=2;
		if (i==1) {
			System.out.println("First son of Kunti is Yudhishthir");
		}
		else if (i==2) {
			System.out.println("second son of Kunti is Bheem");
		}
		else if (i==3) {
			System.out.println("third son of Kunti is Arjun ");
		}
		else if (i==4) {
			System.out.println("fourth son of Kunti is Nakul");
		}
		else {
			System.out.println("fifth son of Kunti is sahadev ");
		}

	}

}
