package com.controlstatement;

public class Switch4 {

	public static void main(String[] args) {
		int p=6;
		switch (p) {
		case 3*1:
			System.out.println("The number is 3");
			break;
		case 3*2:
			System.out.println("The number is 6");
			break;
		case 3*3:
			System.out.println("The number is 9");
             break;
		}

	}

}
