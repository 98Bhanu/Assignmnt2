package com.controlstatement;

public class Switch2 {

	public static void main(String[] args) {
		String s="India";
		 switch (s) {
		 case "Australia":
			 System.out.println("The country selected australia");
			 break;
		 case "India":
			 System.out.println("The country selected is India");
			 break;
		 default :
			 System.out.println("The country selected is America");
		 }

	}

}
