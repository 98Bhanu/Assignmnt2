package com.controlstatement;

public class Switch3 {

	public static void main(String[] args) {
	int  num =5;
	switch(num) {
	case 1:
		System.out.println(" the square of the number is "+(1*num));
	
	case 2:
		System.out.println(" the square of the number is "+(2*num));
	case 3:
		System.out.println(" the square of the number is "+(3*num));
		case 4:
			System.out.println(" the square of the number is "+(4*num));
		case 5:
			System.out.println(" the square of the number is "+(5*num));
	}
	}
}
